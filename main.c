struct shm_buffer {
    struct wl_buffer *wl_buffer;
    void *data;
    int width, height, stride;
    size_t size;
    cairo_surface_t *cairo_surface; /* only used for the on-screen surface */
    bool busy; /* true while the compositor still owns this buffer */
};

static int create_shm_fd(size_t size)
{
    char name[] = "/px-wayland-XXXXXX";
    /* memfd_create avoids touching the filesystem at all */
    int fd = -1;
#if defined(__linux__)
    fd = syscall(SYS_memfd_create, "px-shm", 0);
#endif
    if (fd < 0) {
        /* fallback: shm_open with a unique name then unlink */
        for (int i = 0; i < 100; i++) {
            snprintf(name, sizeof(name), "/px-wayland-%06x", rand() & 0xffffff);
            fd = shm_open(name, O_RDWR | O_CREAT | O_EXCL, 0600);
            if (fd >= 0) { shm_unlink(name); break; }
            if (errno != EEXIST) break;
        }
    }
    if (fd < 0) return -1;
    if (ftruncate(fd, (off_t)size) < 0) { close(fd); return -1; }
    return fd;
}

static struct shm_buffer *shm_buffer_create(struct wl_shm *shm,
                                             int width, int height,
                                             uint32_t format)
{
    int stride = width * 4;
    size_t size = (size_t)stride * (size_t)height;

    int fd = create_shm_fd(size);
    if (fd < 0) { perror("create_shm_fd"); return NULL; }

    void *data = mmap(NULL, size, PROT_READ | PROT_WRITE, MAP_SHARED, fd, 0);
    if (data == MAP_FAILED) { perror("mmap"); close(fd); return NULL; }

    struct wl_shm_pool *pool = wl_shm_create_pool(shm, fd, (int32_t)size);
    struct wl_buffer *buffer = wl_shm_pool_create_buffer(
        pool, 0, width, height, stride, format);
    wl_shm_pool_destroy(pool);
    close(fd);

    struct shm_buffer *sb = calloc(1, sizeof(*sb));
    sb->wl_buffer = buffer;
    sb->data = data;
    sb->width = width;
    sb->height = height;
    sb->stride = stride;
    sb->size = size;
    return sb;
}

static void shm_buffer_destroy(struct shm_buffer *sb)
{
    if (!sb) return;
    if (sb->cairo_surface) cairo_surface_destroy(sb->cairo_surface);
    if (sb->wl_buffer) wl_buffer_destroy(sb->wl_buffer);
    if (sb->data) munmap(sb->data, sb->size);
    free(sb);
}

