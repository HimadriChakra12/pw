static void ensure_draw_buffers(int width, int height)
{
    for (int i = 0; i < 2; i++) {
        if (A.draw_bufs[i]) shm_buffer_destroy(A.draw_bufs[i]);
        A.draw_bufs[i] = shm_buffer_create(A.shm, width, height, WL_SHM_FORMAT_ARGB8888);
        A.draw_bufs[i]->cairo_surface = cairo_image_surface_create_for_data(
            (unsigned char *)A.draw_bufs[i]->data, CAIRO_FORMAT_ARGB32,
            width, height, A.draw_bufs[i]->stride);
        wl_buffer_add_listener(A.draw_bufs[i]->wl_buffer, &buffer_release_listener, A.draw_bufs[i]);
    }
}

static void layer_surface_configure(void *data,
                                     struct zwlr_layer_surface_v1 *surf,
                                     uint32_t serial, uint32_t width, uint32_t height)
{
    (void)data;
    A.surf_width = (int32_t)width;
    A.surf_height = (int32_t)height;
    zwlr_layer_surface_v1_ack_configure(surf, serial);
    ensure_draw_buffers(A.surf_width, A.surf_height);
    A.configured = true;
    render_frame();
}

static void layer_surface_closed(void *data, struct zwlr_layer_surface_v1 *surf)
{
    (void)data; (void)surf;
    A.running = false;
}

static const struct zwlr_layer_surface_v1_listener layer_surface_listener = {
    .configure = layer_surface_configure,
    .closed = layer_surface_closed,
};

