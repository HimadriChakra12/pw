/* px - Wayland color picker (wlroots-only: needs wlr-layer-shell + wlr-screencopy)
 *
 * Port of the X11 px tool. Differences forced by the Wayland model:
 *
 *   - No XGetImage-on-root: pixel sampling instead comes from a single
 *     wlr-screencopy capture of the whole output, taken once at startup
 *     and held in memory. The magnifier/swatch therefore show a frozen
 *     frame of the desktop, not a live feed - this is the standard
 *     approach used by other Wayland pickers (hyprpicker, wl-color-picker)
 *     because screencopy is async/frame-based, not a synchronous
 *     "sample this pixel now" call.
 *
 *   - No floating root-positioned override-redirect windows: Wayland gives
 *     clients no global screen coordinate space to place windows in.
 *     Instead we open ONE fullscreen zwlr_layer_surface_v1 on the overlay
 *     layer with exclusive keyboard interactivity (replaces XGrabPointer +
 *     XGrabKeyboard), and draw the magnifier + swatch as panels positioned
 *     in *surface-local* coordinates near the simulated pointer.
 *
 *   - Single-output only: this build picks the first wl_output advertised
 *     and ignores the rest (multi-monitor correctness was explicitly
 *     dropped per user request, to keep this from becoming an output-
 *     hotplug/per-output-surface project).
 */

