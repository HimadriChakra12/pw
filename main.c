/* Same logic as the X11 place_win(), but operating in surface-local
 * (== output-local, since we're fullscreen) coordinates instead of
 * moving a separate top-level window. */
static void place_panel(int mx, int my, int sw, int sh,
                         int win_w, int win_h,
                         bool right_side, bool bottom_side,
                         int *out_x, int *out_y)
{
    *out_x = right_side  ? mx + GAP : mx - GAP - win_w;
    *out_y = bottom_side ? my + GAP : my - GAP - win_h;
    if (*out_x < 0)          *out_x = 0;
    if (*out_y < 0)          *out_y = 0;
    if (*out_x + win_w > sw) *out_x = sw - win_w;
    if (*out_y + win_h > sh) *out_y = sh - win_h;
}

/* Read one pixel from the frozen capture buffer.
 *
 * Safe to assume XRGB/ARGB byte order unconditionally here: whatever
 * format the compositor actually handed back in frame_buffer(), it's
 * already been normalized to RGB order in-place by
 * normalize_capture_to_rgb_order() before A.capture_ready was set, so
 * this never runs against raw BGR data. (See that function's comment
 * for why the original version of this code - which assumed RGB order
 * without checking - produced a colour tint on compositors that hand
 * back XBGR8888/ABGR8888.) */
static void sample_capture(int x, int y, int *r, int *g, int *b)
{
    if (!A.capture || x < 0 || y < 0 || x >= A.capture->width || y >= A.capture->height) {
        *r = *g = *b = 0;
        return;
    }
    uint8_t *row = (uint8_t *)A.capture->data + (size_t)y * A.capture->stride;
    uint32_t px;
    memcpy(&px, row + (size_t)x * 4, 4);
    *b = (px >>  0) & 0xff;
    *g = (px >>  8) & 0xff;
    *r = (px >> 16) & 0xff;
}

static cairo_surface_t *capture_as_cairo_surface(void)
{
    if (!A.capture) return NULL;
    /* Safe to wrap the buffer directly with no copy: by this point
     * A.capture_format has been normalized (in frame_ready, via
     * normalize_capture_to_rgb_order) to always be either ARGB8888 or
     * XRGB8888, both of which match cairo's ARGB32/RGB24 byte layout
     * exactly. Cairo has no native format for the BGR variants the
     * compositor may have originally handed back, which is why that
     * normalization step exists rather than branching here. */
    cairo_format_t fmt = (A.capture_format == WL_SHM_FORMAT_ARGB8888)
                          ? CAIRO_FORMAT_ARGB32 : CAIRO_FORMAT_RGB24;
    return cairo_image_surface_create_for_data(
        (unsigned char *)A.capture->data, fmt,
        A.capture->width, A.capture->height, A.capture->stride);
}

static void draw_magnifier(cairo_t *cr, int panel_x, int panel_y, int mx, int my)
{
    int sw = A.capture ? A.capture->width  : A.surf_width;
    int sh = A.capture ? A.capture->height : A.surf_height;

    int sx = mx - HALF, sy = my - HALF;
    if (sx < 0) sx = 0;
    if (sy < 0) sy = 0;
    if (sx + GRID_CELLS > sw) sx = sw - GRID_CELLS;
    if (sy + GRID_CELLS > sh) sy = sh - GRID_CELLS;

    cairo_save(cr);
    cairo_translate(cr, panel_x, panel_y);

    for (int row = 0; row < GRID_CELLS; row++) {
        for (int col = 0; col < GRID_CELLS; col++) {
            int r, g, b;
            sample_capture(sx + col, sy + row, &r, &g, &b);
            double cx = col * CELL_PX, cy = row * CELL_PX;
            bool top = (row == 0), bot = (row == GRID_CELLS - 1);
            bool left = (col == 0), right = (col == GRID_CELLS - 1);
            bool corner = (CORNER_R > 0) && (top || bot) && (left || right);

            cairo_set_source_rgb(cr, r / 255.0, g / 255.0, b / 255.0);

            if (corner) {
                /* rounded outer corners only, square everywhere else */
                double rad = CORNER_R;
                cairo_new_path(cr);
                double x0 = cx, y0 = cy, x1 = cx + CELL_PX, y1 = cy + CELL_PX;
                double rad_tl = (top && left)   ? rad : 0;
                double rad_tr = (top && right)  ? rad : 0;
                double rad_bl = (bot && left)   ? rad : 0;
                double rad_br = (bot && right)  ? rad : 0;
                cairo_move_to(cr, x0 + rad_tl, y0);
                cairo_line_to(cr, x1 - rad_tr, y0);
                if (rad_tr > 0) cairo_arc(cr, x1 - rad_tr, y0 + rad_tr, rad_tr, -M_PI/2, 0);
                else cairo_line_to(cr, x1, y0);
                cairo_line_to(cr, x1, y1 - rad_br);
                if (rad_br > 0) cairo_arc(cr, x1 - rad_br, y1 - rad_br, rad_br, 0, M_PI/2);
                else cairo_line_to(cr, x1, y1);
                cairo_line_to(cr, x0 + rad_bl, y1);
                if (rad_bl > 0) cairo_arc(cr, x0 + rad_bl, y1 - rad_bl, rad_bl, M_PI/2, M_PI);
                else cairo_line_to(cr, x0, y1);
                cairo_line_to(cr, x0, y0 + rad_tl);
                if (rad_tl > 0) cairo_arc(cr, x0 + rad_tl, y0 + rad_tl, rad_tl, M_PI, 3*M_PI/2);
                else cairo_line_to(cr, x0, y0);
                cairo_close_path(cr);
                cairo_fill(cr);
            } else {
                cairo_rectangle(cr, cx, cy, CELL_PX, CELL_PX);
                cairo_fill(cr);
            }

            /* grid lines */
            cairo_set_source_rgb(cr, 0, 0, 0);
            cairo_set_line_width(cr, 1);
            if (col < GRID_CELLS - 1) {
                cairo_move_to(cr, cx + CELL_PX - 0.5, cy);
                cairo_line_to(cr, cx + CELL_PX - 0.5, cy + CELL_PX);
                cairo_stroke(cr);
            }
            if (row < GRID_CELLS - 1) {
                cairo_move_to(cr, cx, cy + CELL_PX - 0.5);
                cairo_line_to(cr, cx + CELL_PX, cy + CELL_PX - 0.5);
                cairo_stroke(cr);
            }
        }
    }

    /* center cell highlight outline, same as X11 version */
    double ccx = HALF * CELL_PX, ccy = HALF * CELL_PX;
    cairo_set_source_rgb(cr, 1, 1, 1);
    cairo_set_line_width(cr, 1);
    cairo_rectangle(cr, ccx + 0.5, ccy + 0.5, CELL_PX - 1, CELL_PX - 1);
    cairo_stroke(cr);

    /* border around whole magnifier */
    cairo_set_source_rgb(cr, 0x44/255.0, 0x44/255.0, 0x44/255.0);
    cairo_set_line_width(cr, 1);
    cairo_rectangle(cr, 0.5, 0.5, WIN_SIZE - 1, WIN_SIZE - 1);
    cairo_stroke(cr);

    cairo_restore(cr);
}

static void draw_swatch(cairo_t *cr, int panel_x, int panel_y,
                         int r, int g, int b)
{
    cairo_save(cr);
    cairo_translate(cr, panel_x, panel_y);

    cairo_set_source_rgb(cr, 0x1a/255.0, 0x1a/255.0, 0x1a/255.0);
    cairo_rectangle(cr, 0, 0, SW_W, SW_H);
    cairo_fill(cr);

    cairo_set_source_rgb(cr, r / 255.0, g / 255.0, b / 255.0);
    cairo_rectangle(cr, 0, 0, SW_W / 3.0, SW_H);
    cairo_fill(cr);

    cairo_set_source_rgb(cr, 0x44/255.0, 0x44/255.0, 0x44/255.0);
    cairo_set_line_width(cr, 1);
    cairo_move_to(cr, SW_W / 3.0, 0);
    cairo_line_to(cr, SW_W / 3.0, SW_H);
    cairo_stroke(cr);

    char hex[12], rgb_str[24];
    snprintf(hex, sizeof(hex), "#%02x%02x%02x", r, g, b);
    snprintf(rgb_str, sizeof(rgb_str), "%d %d %d", r, g, b);

    cairo_set_source_rgb(cr, 1, 1, 1);
    cairo_select_font_face(cr, "monospace", CAIRO_FONT_SLANT_NORMAL, CAIRO_FONT_WEIGHT_NORMAL);
    cairo_set_font_size(cr, 13);
    cairo_move_to(cr, SW_W / 3.0 + 7, 22);
    cairo_show_text(cr, hex);
    cairo_move_to(cr, SW_W / 3.0 + 7, 40);
    cairo_show_text(cr, rgb_str);

    cairo_set_source_rgb(cr, 0x44/255.0, 0x44/255.0, 0x44/255.0);
    cairo_rectangle(cr, 0.5, 0.5, SW_W - 1, SW_H - 1);
    cairo_stroke(cr);

    cairo_restore(cr);
}

/* Render one full frame: background = frozen desktop capture (so the
 * picker still "sees" the screen), plus magnifier + swatch panels near
 * the simulated pointer. Mirrors update_both() from the X11 version. */
static void render_frame(void)
{
    if (!A.configured || A.surf_width <= 0 || A.surf_height <= 0) return;

    /* pick whichever of the two draw buffers the compositor isn't
     * currently holding onto; if both are busy (shouldn't normally
     * happen with only two in flight) dispatch until one frees up,
     * rather than attaching a buffer the compositor may still be
     * reading from. */
    int idx = A.cur_buf;
    if (A.draw_bufs[idx]->busy) idx ^= 1;
    int spins = 0;
    while (A.draw_bufs[idx]->busy && spins++ < 50) {
        wl_display_dispatch_pending(A.display);
        wl_display_flush(A.display);
    }
    struct shm_buffer *buf = A.draw_bufs[idx];
    if (!buf) return;

    cairo_t *cr = cairo_create(buf->cairo_surface);

    /* background: the frozen capture, scaled/positioned 1:1 */
    cairo_surface_t *bg = capture_as_cairo_surface();
    if (bg) {
        cairo_set_source_surface(cr, bg, 0, 0);
        cairo_paint(cr);
        cairo_surface_destroy(bg);
    } else {
        cairo_set_source_rgb(cr, 0, 0, 0);
        cairo_paint(cr);
    }

    int mx = (int)A.ptr_x, my = (int)A.ptr_y;
    int sw = A.surf_width, sh = A.surf_height;

    bool prev_right  = (mx + GAP + WIN_SIZE <= sw);
    bool prev_bottom = (my + GAP + WIN_SIZE <= sh);
    bool sw_right  = !prev_right;
    bool sw_bottom = prev_bottom;

    int px, py, swx, swy;
    place_panel(mx, my, sw, sh, WIN_SIZE, WIN_SIZE, prev_right, prev_bottom, &px, &py);
    place_panel(mx, my, sw, sh, SW_W, SW_H, sw_right, sw_bottom, &swx, &swy);

    draw_magnifier(cr, px, py, mx, my);

    int r, g, b;
    sample_capture(mx, my, &r, &g, &b);
    draw_swatch(cr, swx, swy, r, g, b);

    cairo_destroy(cr);

    buf->busy = true;
    wl_surface_attach(A.surface, buf->wl_buffer, 0, 0);
    wl_surface_damage_buffer(A.surface, 0, 0, A.surf_width, A.surf_height);
    wl_surface_commit(A.surface);

    A.cur_buf = idx ^ 1;
}

